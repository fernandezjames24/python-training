
class Vehicle:
    name = ""
    kind = "car"
    color = ""
    value = 100.00
    def description(self):
        desc_str = "%s is a %s %s worth $%.2f." % (self.name, self.color, self.kind, self.value)
        return desc_str

#instance of Vehicle
vehicle = Vehicle()

#print all attribute of instance vehicle
print(dir(vehicle))
